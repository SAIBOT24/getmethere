const ARRAY_PAIRS = [
	"redgray",
	"yellowpurple",
	"orangegreen",
	"brownblue"
]
const SEED = "TncqMTtsmsy";
const UPDATE_RESOLUTION = 24;

export default function getWord() {
	let item1, item2;
	let num = (Math.floor(Math.floor(new Date().getTime() / 3600000) / UPDATE_RESOLUTION));
	let text = md5(`${num.toString(10)}|${UPDATE_RESOLUTION.toString(10)}|${SEED}`).toString();

	let num2 = Number.parseInt(text.substring(0,1), 16);
	if (num2 > 7)
		num2 -= 8;
	if (num2 > 3)
		num2 -= 4;
	item1 = ARRAY_PAIRS[num2];

	let num3 = Number.parseInt(text.substring(1,3), 16);
	item2 = WORDS[num3];

	return [item1,item2];
}
